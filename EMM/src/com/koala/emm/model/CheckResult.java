package com.koala.emm.model;

public class CheckResult {
	private String app_edition;
	private String imei;
	private String package_name;
	private Integer state;
	private String update_time;
	private String update_url;
	private String update_user;
	public String getApp_edition() {
		return app_edition;
	}
	public void setApp_edition(String app_edition) {
		this.app_edition = app_edition;
	}
	public String getImei() {
		return imei;
	}
	public void setImei(String imei) {
		this.imei = imei;
	}
	public String getPackage_name() {
		return package_name;
	}
	public void setPackage_name(String package_name) {
		this.package_name = package_name;
	}
	@Override
	public String toString() {
		return "CheckResult [app_edition=" + app_edition + ", imei=" + imei
				+ ", package_name=" + package_name + ", state=" + state
				+ ", update_time=" + update_time + ", update_url=" + update_url
				+ ", update_user=" + update_user + "]";
	}
	public Integer getState() {
		return state;
	}
	public void setState(Integer state) {
		this.state = state;
	}
	public String getUpdate_time() {
		return update_time;
	}
	public void setUpdate_time(String update_time) {
		this.update_time = update_time;
	}
	public String getUpdate_url() {
		return update_url;
	}
	public void setUpdate_url(String update_url) {
		this.update_url = update_url;
	}
	public String getUpdate_user() {
		return update_user;
	}
	public void setUpdate_user(String update_user) {
		this.update_user = update_user;
	}
	
	
}
